#include <iostream>

using namespace std;

int main() {

    float base, altura, area;
    cout << "Qual é a base do retangulo? ";
    cin >> base;
    cout << "Qual é a altura do retangulo? ";
    cin >> altura;
    area = (base * altura);
    cout << "A area do retangulo é" << area;

    return 0;
}
